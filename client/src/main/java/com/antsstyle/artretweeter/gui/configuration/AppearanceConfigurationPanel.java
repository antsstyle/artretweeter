    private void labelFontColourChooserButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_labelFontColourChooserButtonActionPerformed
        labelFontColourChooserButton.setEnabled(false);
        Color color = JColorChooser.showDialog(
                GUI.getInstance(),
                "Choose Background Color",
                null);
        if (color != null) {
            GUIConfig.WINDOW_FONT_COLOUR = color;
            windowFontColourLabel.setBackground(color);
            GUIHelperMethods.setOppositeColourDashedBorder(windowFontColourLabel);
        }
        GUIHelperMethods.setAllGUIColours();
        labelFontColourChooserButton.setEnabled(true);
    }//GEN-LAST:event_labelFontColourChooserButtonActionPerformed

    private void windowFontColourRevertButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_windowFontColourRevertButtonActionPerformed
        windowFontColourRevertButton.setEnabled(false);
        Color defaultColor = new Color(200, 200, 200);
        GUIConfig.WINDOW_FONT_COLOUR = defaultColor;
        windowFontColourLabel.setBackground(defaultColor);
        GUIHelperMethods.setOppositeColourDashedBorder(windowFontColourLabel);
        String colourString = String.valueOf(defaultColor.getRed()).concat(",").concat(String.valueOf(defaultColor.getGreen()))
                .concat(",").concat(String.valueOf(defaultColor.getBlue()));
        ConfigDB.updateConfigItem(GUIConfig.P_WINDOW_FONT_COLOUR, colourString);
        GUIHelperMethods.setAllGUIColours();
        windowFontColourRevertButton.setEnabled(true);
    }//GEN-LAST:event_windowFontColourRevertButtonActionPerformed

