    @Override
    public void commitChanges() {
        ArrayList<String> namesToUpdate = new ArrayList<>();
        ArrayList<Color> valuesToUpdate = new ArrayList<>();
        namesToUpdate.add(GUIConfig.P_JBUTTON_BG_COLOUR);
        namesToUpdate.add(GUIConfig.P_JBUTTON_FONT_COLOUR);
        namesToUpdate.add(GUIConfig.P_WINDOW_BG_COLOUR);
        namesToUpdate.add(GUIConfig.P_WINDOW_FONT_COLOUR);
        valuesToUpdate.add(GUIConfig.JBUTTON_BG_COLOUR);
        valuesToUpdate.add(GUIConfig.JBUTTON_FONT_COLOUR);
        valuesToUpdate.add(GUIConfig.WINDOW_BG_COLOUR);
        valuesToUpdate.add(GUIConfig.WINDOW_FONT_COLOUR);
        ArrayList<Object[]> params = new ArrayList<>();
        for (int i = 0; i < namesToUpdate.size(); i++) {
            params.add(new Object[]{namesToUpdate.get(i),
                ConfigurationModule.convertConfigItemToString(valuesToUpdate.get(i))});
        }

        boolean success = ConfigDB.updateConfigItems(params);
        if (!success) {
            String message = "Failed to commit appearance changes.";
            JOptionPane.showMessageDialog(GUI.getInstance(), message, "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

